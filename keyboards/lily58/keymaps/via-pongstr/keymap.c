static void print_status_narrow(void) {
    /* Print current mode */
    // oled_set_cursor(0, 0);
    // oled_write_raw_P(mac_logo, sizeof(mac_logo));


    oled_set_cursor(0, 3);
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_ln_P(PSTR(" BASE"), false);
            break;
        case _LOWER:
            oled_write_ln_P(PSTR("LYR 1"), false);
            break;
        case _RAISE:
            oled_write_ln_P(PSTR("LYR 2"), false);
            break;
        case _ADJUST:
            oled_write_ln_P(PSTR("LYR 3"), false);
            break;
        default:
            oled_write_ln_P(PSTR("Undefined"), false);
    }

    /* caps lock */
    oled_set_cursor(0, 5);
    oled_write(" CAPS", led_usb_state.caps_lock);

    render_luna(0, 12);
    oled_set_cursor(1, 15);
    oled_write("TOFU", led_usb_state.caps_lock);

    /* this fixes the screen on and off bug */
    if (current_wpm > 0) {
        oled_on();
        anim_sleep = timer_read32();
    }
}


bool oled_task_user(void) {
    /* KEYBOARD PET VARIABLES START */

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    /* KEYBOARD PET VARIABLES END */
    if (is_keyboard_master()) {
        print_status_narrow();
    } else {
        print_logo_narrow();
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

    }
    return true;
}

